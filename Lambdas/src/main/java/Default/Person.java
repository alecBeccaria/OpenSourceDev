package Default;

public class Person {
    private String strName;
    private String strSSN;
    private String strEmail;
    private String strStreet;
    private String strCity;
    private String strState;
    private String strZip;

    public String getStrName() {
        return strName;
    }

    public void setStrName(String strName) {
        this.strName = strName;
    }

    public String getStrSSN() {
        return strSSN;
    }

    public void setStrSSN(String strSSN) {
        this.strSSN = strSSN;
    }

    public String getStrEmail() {
        return strEmail;
    }

    public void setStrEmail(String strEmail) {
        this.strEmail = strEmail;
    }

    public String getStrStreet() {
        return strStreet;
    }

    public void setStrStreet(String strStreet) {
        this.strStreet = strStreet;
    }

    public String getStrCity() {
        return strCity;
    }

    public void setStrCity(String strCity) {
        this.strCity = strCity;
    }

    public String getStrState() {
        return strState;
    }

    public void setStrState(String strState) {
        this.strState = strState;
    }

    public String getStrZip() {
        return strZip;
    }

    public void setStrZip(String strZip) {
        this.strZip = strZip;
    }
}
